package com.nhpost.post.web.model;

public class ViewPost {
	public String postText;
	public String postRating;
	public String likeCount;
	/**
	 * @return the postText
	 */
	public String getPostText() {
		return postText;
	}
	/**
	 * @param postText the postText to set
	 */
	public void setPostText(String postText) {
		this.postText = postText;
	}
	/**
	 * @return the postRating
	 */
	public String getPostRating() {
		return postRating;
	}
	/**
	 * @param postRating the postRating to set
	 */
	public void setPostRating(String postRating) {
		this.postRating = postRating;
	}
	/**
	 * @return the likeCount
	 */
	public String getLikeCount() {
		return likeCount;
	}
	/**
	 * @param likeCount the likeCount to set
	 */
	public void setLikeCount(String likeCount) {
		this.likeCount = likeCount;
	}
	
	
}
